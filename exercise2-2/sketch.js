function setup() {
    createCanvas(800,800)
}

function draw() {
   let centreX=400
   let centreY=300
    stroke(0)
    strokeWeight(1)
    background(255)
    rectMode(CENTER)
    fill (255,255,0)
    square(centreX,centreY,200)
    fill(255)
    ellipse(centreX-50,centreY-50,40,60)
    ellipse(centreX+50,centreY-50,40,60)
    fill(0)
    circle(centreX-50,centreY-50,5)
    circle(centreX+50,centreY-50,5)
    noStroke()
    fill(255,255,0)
    rect(centreX-50,centreY-80,60,30)
    rect(centreX+50,centreY-80,60,30)
    fill(0)
    stroke(0)
    line(centreX-20,centreY-20,360,340)
    line(centreX+20,centreY-20,440,340)
    circle(centreX-20,centreY+30,15)
    circle(centreX+20,centreY+30,15)
    fill(255,0,0)
    noStroke()
    rect(centreX,centreY+60,50,5)
    fill(0)
    rect(centreX,centreY-65,150,15)
    rectMode(CORNER)
    stroke(0)
    fill(255)
    rect(centreX-100,centreY+100,200,20)
    fill(150,75,0)
    rect(centreX-100,centreY+120,200,20)
    fill(300,300,0)
    rect(centreX-40,centreY+140,5,100)
    rect(centreX+30,centreY+140,5,100)
    fill(255,255,0)
    strokeWeight(3)
    line(centreX-100,centreY+20,240,350)
    line(centreX+100,centreY+20,560,350)

}